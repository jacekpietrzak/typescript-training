/**
 * Number type
 * */

let number: number = 100;

number = 28.7;
number = -30;

// number expression
number = Infinity * 0.01; // this evaluates to some kind of number hence it can be assigned to number type variable.
number = parseInt("12"); // parseInt converts this string into a number and can be assigned to number type variable.
number = parseInt("12saddasdao"); // this converts to 12 no matter how many string letters there are and can be assigned to number type variable.
number = parseInt("oasdadads"); // this converts to NaN and can be assigned to number type variable.
