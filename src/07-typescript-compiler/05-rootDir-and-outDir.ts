/** rootDir
 * - In tsconfig file we can find rootDir
 * - lets uncomment it to turn this on.
 * - we can set it as './src'
 * - Lets create src directory in main directory and move app.ts to src folder
 * - now all files inside rootDir will be compiled by TS
 */

/** outDir
 * - lets you define an output directory for all the JS files. It tells TS to compile all JS files to this directory.
 * - lets create a dist folder in our root directory
 * - uncomment outDir
 * - set folder to './dist'
 * - will follow a folder hierarchy from src folder
 */
